function buildLine(template) {
    return template.replace(/\{(\w+)\}/g, (_, key) => {
        const pool = POOLS[key];
        if (!pool || pool.length === 0) return `{${key}}`;
        return pool[Math.floor(Math.random() * pool.length)];
    });
}

